/*
* A Java program that takes two numbers and print their sum.
*/

public class Sum{
    public static void main(String[] args){
        
        int firstNum = 45;
        int secondNum = 78;
        int sum = firstNum + secondNum;
        System.out.printf("The sum of %d and %d is %d%n", firstNum, secondNum, sum);
    }
}
