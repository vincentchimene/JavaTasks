/**
*3. Write a program that prints the multiplication table of 5.(from 1x to 12x).
*
*/

public class FiveTimesTable{
    public static void main(String[] args){
        for (int count = 1; count <=12; count++){
            System.out.printf("%n5 X %d  =  %d%n", count, count*5);
        } 
    }

}
